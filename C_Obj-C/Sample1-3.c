#include <stdio.h>

int main(int argc, const char * argv[]){

    int x = 1234;

    int y = 56;

    int a1 = x + y;

    int a2 = x - y;

    int a3 = x * y;

    int a4 = x / y;

    printf("A + B = %i\n", a1);

    printf("A - B = %i\n", a2);

    printf("A * B = %i\n", a3);

    printf("A / B = %i\n", a4);

    return 0;

}

